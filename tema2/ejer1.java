public class ejer1 {
	public static void main(String[] args) {
		int x;
		int y;
		x = 144;
		y = 999;

		System.out.println("x: " + x);
		System.out.println("y: " + y);
		System.out.printf("Suma: %d\n", x+y);
		System.out.printf("Resta: %d\n", x-y);
		System.out.printf("Producto: %d\n", x*y);
		System.out.printf("Division: %d\n", x/y);
	}
}